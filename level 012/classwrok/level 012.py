#  Logical operators - გამოიყენება პირობითი გამოხატულებების შესაერთებლად

# 1) and → აბრუნებს True-ს მხოლოდ მაშინ, როცა ორივე პირობა True
#   True and True - True
#   True and False - False

# 2) or → აბრუნებს True-ს, თუ მინიმუმ ერთი პირობა Trueა
#   True or False - True
#   False or False - False

# 2 გვერით პასუხებიც 
# 1) >
print(7 > 3)  # True

# 2) <
print(4 < 1)  # False

# 3) >=
print(10 >= 10)  # True

# 4) <=
print(6 <= 8)  # True

# 5) ==
print(5 == 2)  # False

# 3

# and
print(5 > 2 and 10 > 3)   # True 

#  or
print(7 == 1 or 4 < 10)   # True 

