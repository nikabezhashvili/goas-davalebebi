# 1
text = "Hello World"

# upper() - გადააქცევს დიდ ასოებად
print(text.upper())

# lower() - გადააქცევს პატარა ასოებად
print(text.lower())

# replace() - შეცვლის სიტყვას
print(text.replace("World", "Python"))

# len() - სტრინგის სიგრძე
print(len(text))

# strip() - აშორებს ცარიელ ადგილებს დასაწყისსა და ბოლოში
text2 = "   hi   "
print(text2.strip())

# capitalize() - პირველი ასოს წერს დედ რეგისტრში ხოლო დანარჩენს პატარა რეგისტრში
print(text.capitalize())

